    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        System.out.println("Getting Criminal Id");
        String sid = jTextField1.getText();
        if (sid == null || sid.trim().equals("")) {
            System.out.println("Id can't be blank");
            JOptionPane.showMessageDialog(CriminalImgDetailDelete.this, "Id can't be blank");
        } else {
            int id = Integer.parseInt(sid);
            int i = DaoCriminalImgDetail.delete(id);
            if (i > 0) {
                System.out.println("Record Deleted Successfully");
                JOptionPane.showMessageDialog(CriminalImgDetailDelete.this, "Record deleted successfully!");
                frame.dispose();
            } else {
                System.out.println("Error: Record Not Found");
                JOptionPane.showMessageDialog(CriminalImgDetailDelete.this, "Reord not found to delete given id!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

